int getnum(int i,int j)
{
	int sum,x,y;
	sum = 0;
	x = i;
	y = j;
	while(data[x][y] != '#'){
		if(data[x++][y] == 'G') sum++;
	}
	x = i;
	y = j;
	while(data[x][y] != '#'){
		if(data[x--][y] == 'G') sum++;
	}
	x = i;
	y = j;
	while(data[x][y] != '#'){
		if(data[x][y++] == 'G') sum++;
	}
	x = i;
	y = j;
	while(data[x][y] != '#'){
		if(data[x][y--] == 'G') sum++;
	}
	return sum;
}

